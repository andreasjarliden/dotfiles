-- Tabs
vim.cmd("set expandtab")
vim.cmd("set tabstop=2")
vim.cmd("set softtabstop=2")
vim.cmd("set shiftwidth=2")

-- Cursor style
vim.cmd("set cursorline")
vim.cmd("set number")
vim.cmd("set relativenumber")

-- Use space as leader
vim.g.mapleader = " "

-- Use kk as <Esc> in insert mode
vim.cmd("inoremap kk <Esc>")


